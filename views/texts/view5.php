  <div class="box"><p>
	<strong><span style="font-size:14px;">Как определить свой размер обуви?</span></strong></p>
<p>
	<span style="font-size:14px;">Для определения своего размера обуви нужно измерить длину стопы. Делать это рекомендуется во второй половине дня, потому что длина стопы в это время имеет максимальное значение.</span></p>
<p>
	<span style="font-size: 14px;">1. Встаньте на лист бумаги и обведите стопу карандашом, можно обвести только большой палец и пятку. Пятку обводите по прилеганию стопы к бумаге, немного под углом. (Рис. 1)</span></p>
<p>
	<span style="font-size: 14px;">2. Для определения длины стопы измерьте расстояние между самыми удаленными точками на чертеже, как показано на рисунке. (Рис. 2)</span></p>
<p style="text-align: center;">
	<span style="font-size: 14px;"><img align="bottom" alt="" height="277" src="../images/brends/step_size.jpg" width="547"></span></p>
<p>
	<span style="font-size: 14px;">3. Если для правой и левой ноги длины отличаются, выберите наибольшую. </span></p>
<p>
	<span style="font-size: 14px;">4. Найдите свой размер в таблице.</span></p>
<p>
	<span style="font-size: 14px;"><b>Важно!</b> Обязательно укажите при оформлении заказа длину стопы, <u>измеренную точно по этой инструкции</u>, для уточнения размера. Длину стопы нужно измерить и указать в сантиметрах до десятых (например, 28,3 см).</span></p>
<p style="text-align: center;">
	<img alt="" height="286" src="../images/brends/shoe_size_table.png" width="644"></p>
<p>
	<span style="font-size: 14px;"><b>Внимание!</b> Все размеры обуви, представленной на нашем сайте, приведены к данной таблице. Уточненный подбор размера конкретной выбранной модели по длине и полноте стопы производит наш консультант при обработке заказа.</span></p>
