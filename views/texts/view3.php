<p>
	<span style="font-size:14px;"><strong>Доставка по Украине - Предоплата</strong></span></p>
<hr>
<p>
	<span style="font-size:14px;">Отличие от доставки наложенным платежом, состоит в том, что сначала Вы вносите оплату за товар, а затем забираете его в отделении службы доставки <u>без дополнительных платежей</u>, в этом случае доставка будет <u><b>полностью бесплатной</b></u>.</span></p>
<p>
	<span style="font-size: 14px;">Оплата за товар производится путем перевода денег на расчетный счет или карту «Приватбанк», реквизиты для оплаты сообщаются по смс после подтверждения заказа. Оплату можно совершить в терминалах самообслуживания «Приватбанк», в кассе любого банка или в системе «Приват24».</span></p>
<hr>
<p>
	&nbsp;</p>
<p>
