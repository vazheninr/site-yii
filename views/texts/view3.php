<p>
	<span style="font-size:14px;"><strong>Обмен или возврат товара</strong></span></p>
<p>
	<span style="font-size:14px;">В соответствии со <a href="http://zakon2.rada.gov.ua/laws/show/1023-12/paran133#n133" rel="nofollow" target="_blank">Статьей 9 Закона Украины «О защите прав потребителей»</a> <u>обмен или возврат товара производится в течение 14 дней</u>, не считая дня покупки, если он не подошел по размеру, фасону и тп. Обмен или возврат товара производится если он не использовался, если полностью сохранен его товарный вид, упаковка, ярлыки, а также товарный чек, полученный вместе с товаром.</span></p>
<p>
	<span style="font-size:14px;">Повторная доставка товара при первой замене размера выполняется нами <u><b>бесплатно</b></u>.</span></p>
<p>
	<span style="font-size:14px;">Детально условия возврата и обмена товара согласовываются с менеджером по телефону.</span></p>
<p>
	<span style="font-size:14px;">При каждой покупке в нашем магазине выдается товарный чек с указанием производителя, названия модели, размера, цены, даты покупки, с подписью продавца и печатью.<br>
	</span></p>
<p>
	<span style="font-size:14px;">Возврат уплаченных денег производится посредством денежного перевода на карту "Приватбанк", на карту Visa/MasterCard любого банка, системой денежных переводов PrivatMoney (Приватбанк).</span></p>
