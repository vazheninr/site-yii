  <div class="box"><p>
	<span style="font-size:14px;"><strong>Доставка по Украине - Наложенный платеж (оплата товара при получении)</strong></span></p>
<hr>
<p>
	<span style="font-size:14px;">Доставка наложенным платежом по всей Украине осуществляется службой доставки «Новая почта». Этот способ означает, что Вы получаете и оплачиваете товар в отделении службы доставки, при этом можно осмотреть (примерить) получаемый товар до момента окончательной оплаты, а в случае, если он не подошел, отказаться от покупки.</span></p>
<p>
	<span style="font-size:14px;">Доставка обуви до отделения в Вашем населенном пункте <u><b>бесплатная</b></u>. При покупке Вам нужно будет дополнительно оплатить обратную отправку денег за товар, это 2% от стоимости товара+20 грн. за оформление (например, 37 грн. при стоимости товара 850 грн.).</span></p>
<p>
	<span style="font-size:14px;">При оформлении заказа Вам необходимо указать отделение «Новой почты», из которого будет удобно забрать посылку. При согласовании заказа по телефону наш консультант уточнит все условия доставки и оплаты, а также сообщит когда будет отправка товара.</span></p>
<p>
	<span style="font-size:14px;">Расположение ближайшего отделения «Новой почты» Вы можете посмотреть по ссылке - <a href="http://novaposhta.ua/frontend/brunchoffices/" rel="nofollow" target="_blank">Новая почта - отделения</a>.</span></p>
<p style="text-align: center">
	<a href="http://novaposhta.ua/frontend/brunchoffices/" rel="nofollow" target="_blank"><img alt="Адреса отделений Новой почты" height="79" src="../images/brends/novaposhta_logo_new.jpg" width="385"></a></p>
<p>
	<span style="font-size:14px;">После отправки товара Вы получите сообщение на мобильный телефон с номером накладной, по которому Вы сможете отследить и получить посылку. Отследить перемещение посылки по номеру накладной можно по ссылке - <a href="http://novaposhta.ua/frontend/tracking/ua" rel="nofollow" target="_blank">Новая почта - отследить груз</a>. </span></p>
<p>
	<span style="font-size:14px;">Срок доставки составляет 1-2 суток в зависимости от удаленности населенного пункта. После прибытия посылки, она хранится в отделении 4 дня, на 5 день она автоматически возвращается к нам обратно, поэтому Вам нужно успеть её забрать.</span></p>
<p>
	<span style="font-size:14px;">Для получения посылки Вам нужно иметь с собой паспорт или водительское удостоверение, а также знать номер накладной.</span></p>
<p>
	<span style="font-size:14px;">Если в Вашем населенном пункте нет службы «Новая почта», то существует возможность бесплатной доставки «Укрпочтой» или другими службами доставки.</span></p>
<p>
	<span style="font-size:14px;">Более подробно объяснить интересующие детали доставки, оплаты и получения товара Вам сможет консультант при обработке заказа или по телефонам, указанным в контактах.</span></p>
